export default () => {
  const [refDiv, { width, height }] = useMeasure({ polyfill: ResizeObserver });
  const [img, setImg] = useState("pax");
  const { dark } = useContext(ThemeContext);
  const smallSize = width < height ? width : height;
  const size = smallSize / numRows;

  const toggle = () => {
    if (img === "pax") {
      setImg("agent");
      window.document.title = "KILL AGENT";
    } else {
      setImg("pax");
      window.document.title = "WORSHIP PAX";
    }
  };

  const colors = {
    blue: dark ? "#3BA5C8" : "#FCEE21",
    yellow: dark ? "#FCEE21" : "#3BA5C8",
    bg: "#1A1B1E",
  };

  return (
    <div
      onClick={toggle}
      ref={refDiv}
      style={{
        width: "100%",
        height: "100vh",
        display: "flex",
        justifyContent: "center",
        alignItems: "center",
        cursor: "pointer",
        background: colors.bg,
      }}
    >
      <div
        className="pax"
        style={{
          display: "grid",
          gridTemplateColumns: `repeat(${numCols}, ${size}px)`,
        }}
      >
        {Array(numRows * numCols)
          .fill(0)
          .map((_, i) => {
            let id = i++;
            return (
              <div
                key={`pax-${id}`}
                className="paxGrid"
                style={{
                  width: size,
                  height: size,
                  background: imgs[`${img}Blue`].includes(id)
                    ? colors.blue
                    : imgs[`${img}Yellow`].includes(id)
                    ? colors.yellow
                    : colors.bg,
                  transition: `all 1200ms, background 10ms`,
                }}
              />
            );
          })}
      </div>
    </div>
  );
};
